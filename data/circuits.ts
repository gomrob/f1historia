export function getCircuit(id: string): Circuit | undefined {
  return CIRCUITS.find(c => c.id === id)
}

export function getActiveCircuits(): Circuit[] {
  return CIRCUITS.filter(c => c.active)
}

export function searchCircuits(query: string): Circuit[] {
  const q = query.toLowerCase()
  return CIRCUITS.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.country.toLowerCase().includes(q) ||
    c.city.toLowerCase().includes(q)
  )
}
